[y,fs]=audioread('111.wav');
ts=1/fs;
t=[(1/fs):(1/fs):((length(y))/fs)];
figure(1);
subplot(5,2,1);
plot(t,y);
title('原始信号波形');
xlabel('时间 t');
grid on; 
subplot(5,2,2);
num=length(y);
plot((-num/2:num/2-1)/num*fs,abs(fftshift(fft(y)))); 
title('原始信号频谱');
xlabel('frequence Hz');
grid on;

