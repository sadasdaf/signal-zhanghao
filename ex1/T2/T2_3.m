g2 = heaviside(4 - 2 * t + 2) - heaviside(4 - 2 * t - 2);
g4 = heaviside(4 - 2 * t + 4) - heaviside(4 - 2 * t - 4);
f2 = g2 + g4;
f3 = diff(f2);
plot(t(1:end-1),f3,'r');
axis([-11 11 -1 2.5]);
xlabel("蓝色为f(t),红色为f'(4 - 2t)");
title("f(t)与f'(4 - 2t)");